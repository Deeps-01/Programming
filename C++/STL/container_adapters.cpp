// They're special predefined containersv which can be adapted to
// certain uses.
// stack - Works LIFO they use deque as their default containers, but
//          can be adapted to use vector or lists
//
// queue - Works FIFO. Defaults to using deque, but can use lists also
//
// priority queue - Sorted queue, default sorting operation is operator<.
//                  Removing an element from the front returns the highest
// priority item in the priority queue. 
